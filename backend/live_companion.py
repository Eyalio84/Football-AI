# ─── Data Structures ──────────────────────────────────────────────


@dataclass
class MatchEvent:
    """A single detected event in a live match."""
    event_type: str  # kickoff, goal, goal_conceded, halftime, second_half, fulltime
    minute: str      # e.g., "23'" or "HT" or "FT"
    description: str  # Short factual description
    reaction: str     # Fan persona reaction (generated by Haiku)
    score_home: int
    score_away: int
    timestamp: str = field(default_factory=lambda: datetime.utcnow().isoformat())


@dataclass
class MatchState:
    """Tracks the state of a single live match."""
    match_id: int
    home_team: str
    away_team: str
    home_score: int = 0
    away_score: int = 0
    status: str = "TIMED"  # TIMED, IN_PLAY, PAUSED, FINISHED
    minute: int = 0
    events: List[MatchEvent] = field(default_factory=list)
    last_poll: float = 0.0


# ─── Event Detection ──────────────────────────────────────────────


def detect_changes(prev: MatchState, current_data: Dict) -> List[Dict]:
    """Compare previous state to new API data and return detected events."""
    events = []
    new_status = current_data.get("status", prev.status)
    new_home = current_data.get("home_score", prev.home_score) or 0
    new_away = current_data.get("away_score", prev.away_score) or 0
    minute = current_data.get("minute", prev.minute) or 0

    # Kickoff
    if prev.status in ("TIMED", "SCHEDULED") and new_status == "IN_PLAY":
        events.append({
            "type": "kickoff",
            "minute": "0'",
            "desc": f"Kick-off! {prev.home_team} vs {prev.away_team} is underway!",
            "home": new_home, "away": new_away,
        })

    # Goal scored (home)
    if new_home > prev.home_score:
        for _ in range(new_home - prev.home_score):
            events.append({
                "type": "goal",
                "minute": f"{minute}'",
                "desc": f"GOAL! {prev.home_team} score! {new_home}-{new_away}",
                "home": new_home, "away": new_away,
            })

    # Goal scored (away)
    if new_away > prev.away_score:
        for _ in range(new_away - prev.away_score):
            events.append({
                "type": "goal_conceded" if True else "goal",  # perspective handled by reactor
                "minute": f"{minute}'",
                "desc": f"GOAL! {prev.away_team} score! {new_home}-{new_away}",
                "home": new_home, "away": new_away,
            })

    # Half-time
    if prev.status == "IN_PLAY" and new_status == "PAUSED":
        events.append({
            "type": "halftime",
            "minute": "HT",
            "desc": f"Half-time. {prev.home_team} {new_home}-{new_away} {prev.away_team}",
            "home": new_home, "away": new_away,
        })

    # Second half
    if prev.status == "PAUSED" and new_status == "IN_PLAY":
        events.append({
            "type": "second_half",
            "minute": "46'",
            "desc": f"Second half underway. {prev.home_team} {new_home}-{new_away} {prev.away_team}",
            "home": new_home, "away": new_away,
        })

    # Full-time
    if prev.status in ("IN_PLAY", "PAUSED") and new_status == "FINISHED":
        if new_home > new_away:
            result = f"{prev.home_team} win!"
        elif new_away > new_home:
            result = f"{prev.away_team} win!"
        else:
            result = "It's a draw!"
        events.append({
            "type": "fulltime",
            "minute": "FT",
            "desc": f"Full-time! {prev.home_team} {new_home}-{new_away} {prev.away_team}. {result}",
            "home": new_home, "away": new_away,
        })

    return events


# ─── Fan Reaction Generator ──────────────────────────────────────


def generate_reaction(event: Dict, club_id: str, home_team: str, away_team: str,
                      is_home_fan: bool) -> str:
    """Generate a short emotional fan reaction to a match event via Haiku."""
    enhanced = fan_enhancements.get_enhanced_persona(club_id, f"match against {away_team if is_home_fan else home_team}")
    mood = enhanced.get("mood", {})
    dialect = enhanced.get("dialect", {})
    dialect_inject = dialect.get("vocab_inject", "") if dialect else ""

    # Determine emotional context
    score_home, score_away = event["home"], event["away"]
    if is_home_fan:
        my_score, their_score = score_home, score_away
        my_team, their_team = home_team, away_team
    else:
        my_score, their_score = score_away, score_home
        my_team, their_team = away_team, home_team

    if my_score > their_score:
        emotional_state = "ECSTATIC — we're winning!"
    elif my_score < their_score:
        emotional_state = "FURIOUS — we're losing!"
    else:
        emotional_state = "TENSE — it's level!"

    event_desc = event["desc"]
    event_type = event["type"]

    system = f"""You are a passionate {my_team} fan watching a live match.
CURRENT SCORE: {home_team} {score_home}-{score_away} {away_team}
YOUR STATE: {emotional_state}
DIALECT: {dialect_inject}

Generate a SHORT, emotional live reaction (2-3 sentences MAX).
React like a real fan in the stadium — raw emotion, not analysis.
Use your dialect naturally. No hashtags or emojis in text."""

    prompt = f"[{event['minute']}] {event_desc}\n\nReact as a {my_team} fan watching live:"

    try:
        result = ai_response.call_anthropic_api(
            messages=[{"role": "user", "content": prompt}],
            system=system,
        )
        content = result.get("content", [])
        if isinstance(content, list) and content:
            return content[0].get("text", "...")
        return result.get("response", "...")
    except Exception as e:
        logger.warning("Reaction generation failed: %s", e)
        return "..."


