async def run_simulation(match_key: str, club_id: str, queue: asyncio.Queue):
    """
    Replay a pre-recorded match, generating reactions for each event.
    Pushes MatchEvent dicts into the queue for SSE consumption.
    Runs Haiku calls in a thread executor to avoid blocking the event loop.
    """
    script = SIMULATED_MATCHES.get(match_key)
    if not script:
        await queue.put({"type": "error", "message": f"Unknown match: {match_key}"})
        await queue.put(None)
        return

    home_team = script["home_team"]
    away_team = script["away_team"]
    is_home_fan = (club_id == script["home_id"])

    state = MatchState(
        match_id=0,
        home_team=home_team,
        away_team=away_team,
    )

    # Push initial state
    await queue.put({
        "type": "match_info",
        "home_team": home_team,
        "away_team": away_team,
        "club_id": club_id,
    })

    loop = asyncio.get_event_loop()

    for step in script["events"]:
        if step["delay"] > 0:
            await asyncio.sleep(step["delay"])

        current_data = {
            "status": step["status"],
            "home_score": step["home"],
            "away_score": step["away"],
            "minute": step["minute"],
        }

        changes = detect_changes(state, current_data)

        state.home_score = step["home"]
        state.away_score = step["away"]
        state.status = step["status"]
        state.minute = step["minute"]

        for change in changes:
            # Run synchronous Haiku call in thread to avoid blocking
            reaction = await loop.run_in_executor(
                None, generate_reaction,
                change, club_id, home_team, away_team, is_home_fan
            )

            event = {
                "type": "event",
                "event_type": change["type"],
                "minute": change["minute"],
                "description": change["desc"],
                "reaction": reaction,
                "score_home": step["home"],
                "score_away": step["away"],
                "home_team": home_team,
                "away_team": away_team,
                "timestamp": datetime.utcnow().isoformat(),
            }

            await queue.put(event)

    await queue.put({"type": "end", "message": "Match finished"})
    await queue.put(None)


# ─── Live Polling (for real matches) ──────────────────────────────


async def poll_live_match(match_id: int, club_id: str, queue: asyncio.Queue):
    """
    Poll football-data.org for a specific live match.
    Pushes events into queue as they're detected.
    """
    import urllib.request

    api_key = os.environ.get("FOOTBALL_DATA_API_KEY", "")
    headers = {"X-Auth-Token": api_key} if api_key else {}
    url = f"{FOOTBALL_DATA_BASE}/matches/{match_id}"

    state = MatchState(match_id=match_id, home_team="", away_team="")
    initialized = False

    while True:
        try:
            req = urllib.request.Request(url, headers=headers)
            with urllib.request.urlopen(req, timeout=10) as response:
                data = json.loads(response.read().decode("utf-8"))

            match = data
            home_team = match.get("homeTeam", {}).get("name", "Home")
            away_team = match.get("awayTeam", {}).get("name", "Away")
            score = match.get("score", {})
            ft = score.get("fullTime", {})
            status = match.get("status", "TIMED")
            minute = match.get("minute")

            if not initialized:
                state.home_team = home_team
                state.away_team = away_team
                state.status = status
                state.home_score = ft.get("home") or 0
                state.away_score = ft.get("away") or 0
                initialized = True
                await queue.put({
                    "type": "match_info",
                    "home_team": home_team,
                    "away_team": away_team,
                    "club_id": club_id,
                    "match_id": match_id,
                })
                await asyncio.sleep(POLL_INTERVAL)
                continue

            current_data = {
                "status": status,
                "home_score": ft.get("home") or 0,
                "away_score": ft.get("away") or 0,
                "minute": minute or 0,
            }

            changes = detect_changes(state, current_data)

            state.home_score = current_data["home_score"]
            state.away_score = current_data["away_score"]
            state.status = status
            state.minute = minute or 0

            # Determine if fan supports home or away
            club_lower = club_id.lower().replace("_", " ")
            is_home_fan = club_lower in home_team.lower()

            loop = asyncio.get_event_loop()
            for change in changes:
                reaction = await loop.run_in_executor(
                    None, generate_reaction,
                    change, club_id, home_team, away_team, is_home_fan
                )
                await queue.put({
                    "type": "event",
                    "event_type": change["type"],
                    "minute": change["minute"],
                    "description": change["desc"],
                    "reaction": reaction,
                    "score_home": current_data["home_score"],
                    "score_away": current_data["away_score"],
                    "home_team": home_team,
                    "away_team": away_team,
                    "timestamp": datetime.utcnow().isoformat(),
                })

            if status == "FINISHED":
                await queue.put({"type": "end", "message": "Match finished"})
                await queue.put(None)
                return

        except Exception as e:
            logger.warning("Poll error for match %d: %s", match_id, e)

        await asyncio.sleep(POLL_INTERVAL)
